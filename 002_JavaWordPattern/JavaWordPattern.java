public class JavaWordPattern
//this you always need to start java, the things after class is the name of the file
{
    public static void main(String[] args){
        //this is the main method,no semicolon after this but a brace after word for the smaller method
        System.out.println("    J     A     V     V     A   ");
        //remember to add a semicolon after the line, and for this program you just have to count the space
        System.out.println("    J    A A     V   V     A A  ");
        System.out.println("J   J   AAAAA     V V     AAAAA ");
        System.out.println(" J J   A     A     V     A     A");
    }
}