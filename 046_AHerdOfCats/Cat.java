public class Cat {
    String name;
    double weight;
    String breed;
    public Cat(String name, String breed, double weight) {
        this.name = name;
        this.breed = breed;
        this.weight = weight;
    }
    public String getName() {
        return name;
    }
    public double getW() {
        return weight;
    }
    public String getB() {
        return breed;
    }
}